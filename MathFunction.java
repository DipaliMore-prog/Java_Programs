class MathFunction
{
	public static void main(String args[])
	{
		int a=5,b=3,c=-2;
		System.out.println("Squareroot ="+Math.sqrt(a));
		System.out.println("Power ="+Math.pow(a,b));
		System.out.println("Min No ="+Math.min(a,b));
		System.out.println("Max No ="+Math.max(a,b));
		System.out.println("Absolute No ="+Math.abs(c));
	}
}
