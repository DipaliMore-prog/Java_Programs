import java.math.*;
class Mathpkg
{
	public static void main(String args[])
	{
		int a=4,b=3;
		double e=45.68;
		System.out.println("Power of a = "+Math.pow(a,b));
		System.out.println("SquareRoot of a: "+Math.sqrt(a));
		System.out.println("Minimum of a and b: "+Math.min(a,b));
		System.out.println("Maximum of a and b: "+Math.max(a,b));
		System.out.println("Absolute of a: "+Math.abs(e));
		System.out.println("Floor of e: "+Math.floor(e));
		System.out.println("Ceil of e: "+Math.ceil(e));
		System.out.println("Round of e: "+Math.round(e));
		System.out.println("Sin 30 : "+Math.round(Math.sin(30)));
		System.out.println("Sin 90 : "+Math.round(Math.sin(90)));
		System.out.println("Sin 0 : "+Math.round(Math.sin(0)));
		System.out.println("Cos 90 : "+Math.round(Math.cos(90)));
		System.out.println("Sin 45 : "+Math.sin(45));
	}
}
